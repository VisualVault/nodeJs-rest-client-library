            const errorMessage = `GET AGOL publish csv file failed : ${agolFileName} csv file publish failed`;
            logger.error('AGOLPostFponlineDataToAGOLTables: ' + errorMessage + ` [5c]`);
            errorLog.push(errorMessage);
            return;

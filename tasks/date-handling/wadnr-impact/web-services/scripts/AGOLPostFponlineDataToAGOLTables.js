            'https://visualvault.maps.arcgis.com/sharing/rest/content/users/VVAGOL/publish',
            {
                label: `publish(${agolFileName})`,
                retries: 3,
                authentication: appManager,
                params: {
                    itemid: csvItemId,
                    filetype: 'csv',
                    publishParameters: analyzeCsv.publishParameters,
                },
            }

            const errorMessage = `POST AGOL set unique field failed : ${agolFileName} csv file set unique field failed`;
            logger.error('AGOLPostFponlineDataToAGOLTables: ' + errorMessage + ` [5d]`);
            errorLog.push(errorMessage);
            return;
